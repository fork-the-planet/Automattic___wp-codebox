assert.match(executionPhp, /function fuzz_suite_supported_runner_capabilities\(\)/)
assert.match(executionPhp, /function fuzz_suite_runtime_backed_runner_capabilities_contract/)
assert.match(abilitiesPhp, /Run safe PHP in-process WordPress fuzz-suite cases/)
assert.match(abilitiesPhp, /Runtime-backed execution is available through the public wp-codebox CLI or TypeScript facade/)
assert.match(executionPhp, /runtime-action:db_operation/)
assert.match(executionPhp, /wordpress\.db-operation/)
assert.match(executionPhp, /executeWordPressFuzzSuite/)
assert.match(executionPhp, /wp-codebox run-fuzz-suite --runner-mode=runtime-backed/)
assert.match(executionPhp, /'ability_execution_mode'\s*=>\s*'php-in-process-only'/)
assert.match(executionPhp, /'supported_by_this_ability'\s*=>\s*false/)
assert.match(abilitiesPhp, /'supported_runner_capabilities'\s*=>\s*self::fuzz_suite_supported_runner_capabilities\(\)/)
assert.match(abilitiesPhp, /'runtime_backed_execution'\s*=>\s*self::fuzz_suite_runtime_backed_execution_contract\(\)/)
assert.match(executionPhp, /wordpress\.ensure-plugin-active/)
assert.match(executionPhp, /'commands'[\s\S]{0,900}wordpress\.ensure-plugin-active/)
assert.match(executionPhp, /'commands'[\s\S]{0,900}wordpress\.plugin-state/)
assert.match(executionPhp, /WP_CODEBOX_FUZZ_WORKLOAD_ROOT/)
assert.doesNotMatch(executionPhp, /wp_codebox_fuzz_suite_runner_unavailable/)

assert.doesNotMatch(abilitiesPhp + schemasPhp + executionPhp, /WooCommerce|Jetpack|Data Machine/i)

